# Condicional Simples

# Para condicoes em Python utilizamos a palavra if que significa
# 'se' em portugues.
# com a condicional, voce consegue executar um conjunto
# de codigo apenas se o resultado de expessao for "verdadeiro" (true)

idade = int(input("qual a sua idade? "))
if idade >= 18:
     print("maior de idade")
else:
     print("menor de idade")
     